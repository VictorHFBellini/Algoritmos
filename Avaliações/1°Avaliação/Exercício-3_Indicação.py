#Construa um algoritmo que indique se um número digitado está
#compreendido entre 20 e 90 ou não (20 e 90 não estão na faixa de valores)

# __________________________________________________________________________ #

Num = float(input('Digite um número a sua escolha: '))

if (Num > 20 and Num < 90):
  print('O número', Num, 'está compreendido entre os números 20 e 90 !')
else:
  print('O número', Num, 'não está compreendido entre os números 20 e 90 !')
  
