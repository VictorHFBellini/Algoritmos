# Crie um algoritmo que leia um número e apresenta a tabuada deste numero.

# ___________________________Tabuada 3.0___________________________ #
Tabuada = int(input("Insira o Número da Tabuada que você deseja: "))
# _________________________________________________________________ #


# Estrutura de Repetições - Loops #
for n in range(0, 11):
  print(Tabuada, "X", n, "=", Tabuada * n)

# O for é uma váriavel utilizada para o uso de repetições. #
# O in retorna True caso o valor seja encontrado na sequência. #
# O range fornece uma sequência de números inteiros baseada nos argumentos da função. #

